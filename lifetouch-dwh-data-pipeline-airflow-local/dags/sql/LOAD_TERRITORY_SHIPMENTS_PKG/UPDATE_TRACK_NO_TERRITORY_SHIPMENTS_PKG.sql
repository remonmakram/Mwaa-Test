/* TASK No. 4 */
/* Update TRACKING_NO at TERRITORY_SHIPMENTS */

MERGE INTO ODS_OWN.TERRITORY_SHIPMENTS T
USING
    (
         SELECT SL.SHIPMENT_LINE_OID,S.SHIPMENT_OID,S.TRACKING_NO
         FROM 
         ODS_OWN.SHIPMENT_LINE SL,
         ODS_OWN.SHIPMENT S
         WHERE 1 = 1 
         AND SL.SHIPMENT_OID = S.SHIPMENT_OID
         AND S.TRACKING_NO IS NOT NULL       
         AND S.ODS_MODIFY_DATE >= TO_DATE(SUBSTR(:v_cdc_load_date, 1, 19), 'YYYY-MM-DD HH24:MI:SS')  - :v_cdc_oms_overlap  
    )S
    ON (T.SHIPMENT_LINE_OID = S.SHIPMENT_LINE_OID)
        WHEN MATCHED THEN UPDATE
        SET
        T.TRACKING_NO = S.TRACKING_NO,
        T.ODS_MODIFY_DATE = SYSDATE
        WHERE T.TRACKING_NO IS NULL

