	T_TMP_PACKAGE_ITEM_MAX.ITEM_ID	   C1_ITEM_ID,
	T_TMP_PACKAGE_ITEM_MAX.MAX_PACKAGE_ID	   C2_MAX_PACKAGE_ID,
	T_TMP_PACKAGE_ITEM_MAX.ITEM_ID	   C3_ITEM_ID
